class IgraPaint extends CustomPainter {
  final List<List<KletkaModel1>> kletki;
  final List<DrakonModel1> drakoni;

  IgraPaint({required this.kletki, required this.drakoni});
  @override
  void paint(Canvas canvas, Size size) {
    for (final kletkiRow in kletki) {
      for (final kletka in kletkiRow) {
        kletka.drawKletkaD(canvas);
      }
    }
    for (final drakon in drakoni) {
      drakon.drawDrakonD(canvas);
    }
  }

  @override
  bool shouldRepaint(covariant CustomPainter oldDelegate) =>
      this != oldDelegate;
}

